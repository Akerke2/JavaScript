function cardInner() {
    let url = window.location.href;
    let id = url.split("=")[1]; // ТЕҢНЕН ДАЛЬШЕ СПЛИТ АРҚЛЫЛЫ ҚИЫП АЙЛИ АЛЫП АЙДДИДІ
    let product = products.find((p) => p.id == id); // АЙДИДІ САЛЫСТЫРЫП ТҰРМЫЗ


    const cards = document.querySelector(".cards");

    cards.innerHTML = // ИННЕР ХТМЛ АРҚЫЛЫ
        `
              <div id = "${product.id}" class="card">
                <div class = "box-card"> 
                   <div class="card-img">
                     <img src="${product.img}" alt="" />
                     <p class = "card_price">${product.Sub_text}</p>
                     <div class="pad_panel">
                                <button class="add_btn">ADD TO CART</button>
                     </div>
                  </div>
                 <div class="text-card">
                   <h2 class = "card_text_one">${product.title}</h2>
                   <p class = "card_text">${product.text}</p>
                 </div>
                </div>
              </div>
            `;

    const ralph = document.querySelector(".add_btn")

    ralph.addEventListener("click", () => {
        let cart = JSON.parse(localStorage.getItem("cart")) || []; // Локалдан АЛДЫҚ / ЕГЕР ЖОҚ БОЛСА ПУСТОЙ МАССИВ ҚАЙТАРАДЫ
        //localstoragdaN KORZINAГА ALU
        cart.push(product) //КАРТҚА ҚОСТЫМ
        localStorage.setItem("cart", JSON.stringify(cart)) //jsongа ainaldURAMUZ
    })

}
cardInner();




